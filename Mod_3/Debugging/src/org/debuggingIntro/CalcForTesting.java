package org.debuggingIntro;

public class CalcForTesting {
    public static int adding(int a, int b) {
        int first = a;
        int second = b;
        first = first + second;
        return first;
    }
    public static int subtracting(int a, int b) {
        int first = a;
        int second = b;
        first = first - second;
        return first;
    }
    public static int multiplying(int a, int b) {
        int first = a;
        int second = b;
        first = first * second;
        return first;
    }
    public static int dividing(int a, int b) {
        int first = a;
        int second = b;
        first = first / second;
        return first;
    }
}

